package DescomplicaJava;

public class OperadoresAritimeticos {
    public static void main(String[] args) {
        double numA, numB, total;


        numA = 8;
        numB = 10;

        total = numA + numB;
        System.out.println(total);

        total = numA * numB;
        System.out.println(total);

        total = numA % 2;
        System.out.println(total);

    }
    
}
